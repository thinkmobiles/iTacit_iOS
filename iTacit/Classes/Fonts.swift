import UIKit

extension UIFont {

	class func openSansRegular(size: CGFloat) -> UIFont {
		return UIFont(name: "OpenSans", size: size)!
	}

	class func openSansSemibold(size: CGFloat) -> UIFont {
		return UIFont(name: "OpenSans-Semibold", size: size)!
	}
}